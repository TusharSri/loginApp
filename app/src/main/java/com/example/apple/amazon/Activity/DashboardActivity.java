    private void settingDrawer() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        ActionBarDrawerToggle toggle = new ActionBarDrawerToggle(
                this, drawer, toolbar, R.string.navigation_drawer_open, R.string.navigation_drawer_close);
        drawer.addDrawerListener(toggle);
        toggle.syncState();

        NavigationView navigationView = (NavigationView) findViewById(R.id.nav_view);
        navigationView.setNavigationItemSelectedListener(this);
        SharedPreferences prefs = getSharedPreferences("User_Data", MODE_PRIVATE);
        String name = prefs.getString("name", "");
        String email = prefs.getString("email", "");
        Uri url = Uri.parse(prefs.getString("url", ""));
        NavigationView activity_dashboard = findViewById(R.id.nav_view);
        View headerView = activity_dashboard.getHeaderView(0);

        profileImageView = headerView.findViewById(R.id.profile_imageView);
        nameTextView = headerView.findViewById(R.id.name_textView);
        emailTextView = headerView.findViewById(R.id.email_textView);

        Glide.with(this).load(url)
                .thumbnail(0.5f)
                .crossFade()
                .diskCacheStrategy(DiskCacheStrategy.ALL)
                .into(profileImageView);

        nameTextView.setText(name);
        emailTextView.setText(email);
    }

